import java.util.ArrayList;
import java.util.List;

public class Solution {
    public List<List<Integer>> permute(int[] nums) {
        if(nums == null) {
        	return null;
        }
        
        ArrayList<ArrayList<Integer>> permuteSet = new ArrayList<ArrayList<Integer>>();
        if(nums.length == 0) {
        	return (List)permuteSet;
        }
        
    	return null;
    }
}